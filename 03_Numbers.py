# The modulo operator

my_int_1 = 56
my_int_2 = 12

mod_ints = my_int_1 % my_int_2
print(mod_ints)

my_float_1 = 5.4
my_float_2 = 12.0

mod_floats = my_float_2 % my_float_1
print(mod_floats)

# Built-in functions for working with numbers

my_float_3 = -1.1491256
print(round(my_float_3, 3)) 
print(abs(my_float_3))
print(pow(my_float_3, 0))

# Augmented assignment

my_var_1 = 10
my_var_1 += 5
print(my_var_1)

my_var_2 = 27
my_var_2 -= 10
print(my_var_2)

my_var_3 = 13
my_var_3 *= 8
print(my_var_3)

# And we also can use an augmented assignment for /, //, % and **